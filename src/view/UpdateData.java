    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         try
        {
        int n;
        n=JOptionPane.showConfirmDialog(this, "Are you sure want to update", "Save record",JOptionPane.YES_NO_OPTION);
        if(n==0)
        {
            
            String bt,sname,enroll,branch;
            bt=txtbt.getText();
            sname=txtsname.getText();
            enroll=txtenroll.getText();
            branch=txtbranch.getText();
            Data D=new Data();
            D.setBt(bt);
            D.setSnmae(sname);
            D.setEn(enroll);
            D.setBranch(branch);
            InsertDataDao id=new InsertDataDao();
            if(id.update_student(D))
            {
                JOptionPane.showMessageDialog(this, "Record updated...");
            }
           txtbt.setText("");
           txtsname.setText("");
           txtenroll.setText("");
           txtbranch.setText("");
           disable();
           btnUpdate.setEnabled(false);
        }
         
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
     
        String BTno=null;
        BTno=JOptionPane.showInputDialog(this, "Enter BTno for search");
       if(BTno==null)
                 System.out.println("");
      else
       {
         try{  
         InsertDataDao id=new InsertDataDao();
          Data  D=null;
          D=id.searchByBtno(BTno);
           if(D==null)
            JOptionPane.showMessageDialog(this, "Record not found.......");
        else
        {
           txtbt.setText(D.getBt());
           txtsname.setText(D.getSnmae());
           txtenroll.setText(D.getEn());
           txtbranch.setText(D.getBranch());
           enable();
           btnUpdate.setEnabled(true);
        }
        }catch(Exception e){
            System.out.println(e);
       } 
      } 
        
         
    }//GEN-LAST:event_btnSearchActionPerformed

